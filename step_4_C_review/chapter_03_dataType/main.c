#include<stdio.h>

int main()
{
    char a = 'W';
    short b = 1;
    int c = 2;
    long d = 3;
    float e = 2.2;
    double f = 3.3;


    // %c 打印字符
    printf("%c\n", a);
    // %d 打印short int
    printf("%d\n", b);
    printf("%d\n", c);
    // %ld 打印 long
    printf("%ld\n", d);
    // %f 打印 float
    printf("%f\n", e);
    // %lf 打印 double
    printf("%lf\n", f);


    return 1;
}